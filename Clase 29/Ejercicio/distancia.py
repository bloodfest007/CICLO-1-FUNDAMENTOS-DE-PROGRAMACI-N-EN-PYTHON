"""se desea calcular la distancia recorrida (m) por un movil que tiene velocidad constante
 (m/s) durante un tiempo T (seg), considerar que es un MRU"""

velocidad = float(input("Digite velocidad: "))
tiempo = float(input("Digite Tiempo: "))

distancia = velocidad * tiempo

print("La distancia calculada es de " + str(distancia) + " m*s")






